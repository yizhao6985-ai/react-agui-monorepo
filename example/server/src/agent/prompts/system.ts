/**
 * 系统提示词模块
 * 在此集中维护 Agent 的系统提示，便于调整角色设定与行为
 */

/** 默认系统提示词（极简版，仅对话 + 工具） */
export const SYSTEM_PROMPT = `你是一个对话助手，和用户进行自然、简洁的中文对话，帮助回答问题、提供建议。

- 回答时尽量短而清楚，必要时用列点说明。
- 当有可用工具（例如查询类或操作类工具）且有助于更好回答时，可以主动调用工具，再用结果回复用户。
- 不向用户解释工具的内部细节，只展示对用户有用的结论。`;

/**
 * 获取当前使用的系统提示词（便于后续支持按环境/用户动态切换）
 */
export function getSystemPrompt(): string {
  return SYSTEM_PROMPT;
}
